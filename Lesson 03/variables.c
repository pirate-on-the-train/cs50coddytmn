#include <stdio.h>

int main()
{
    // тип имя = значение;
    int a = 5;          // Целочисленная переменная a со значением 5
    float  pi = 3.1415; // Дробная переменная pi со значением 3.1415
    char c = 't';       // Символьная переменная c со значением t

    printf("а = %d\npi = %f\nc = %c\n", a, pi, c);  // Выводим в консоль значения переменных
}